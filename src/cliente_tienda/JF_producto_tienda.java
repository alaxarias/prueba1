    private void BTNborrarproductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNborrarproductoActionPerformed
        // TODO add your handling code here:
         int borrar = Tabla.getSelectedRowCount();
          if(borrar>0){
              tb.removeRow(borrar-1);
              JOptionPane.showMessageDialog(null, "dato borrado satisfactoriamente");
          } else{
              JOptionPane.showMessageDialog(null, "no hay datos","Mensaje",JOptionPane.ERROR_MESSAGE);
          }
        
        
    }//GEN-LAST:event_BTNborrarproductoActionPerformed

    private void BTNborrartodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNborrartodoActionPerformed
        // TODO add your handling code here:
        
         int borrartodo= Tabla.getRowCount();
        for(int i=borrartodo-1;i>-1;i--){
            tb.removeRow(i);
            JOptionPane.showMessageDialog(null, "se borro todo los datos","Mensaje",JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_BTNborrartodoActionPerformed

    private void BTNmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNmodificarActionPerformed
        // TODO add your handling code here:
        
         int modificar = Tabla.getSelectedRow();
          
          if(modificar>-1){
              String[] s={TXTcod.getText(),TXTdesp.getText(),TXTcant.getText(),TXTiva.getText(),TXTcom.getText(),TXTven.getText(),total.toString()};
              tb.insertRow(modificar, s);
              tb.removeRow(modificar+1);
              JOptionPane.showMessageDialog(null, "dato modificado satisfactoriamente","Mensaje",JOptionPane.WARNING_MESSAGE);
          } else{
              JOptionPane.showMessageDialog(null, "no hay datos para modificar","Mensaje",JOptionPane.ERROR_MESSAGE);
          }
        
        
    }//GEN-LAST:event_BTNmodificarActionPerformed

