    private void BTNguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNguardarActionPerformed
        // TODO add your handling code here:
        
        
        String Cod_del_producto= TXTcod.getText();
        String Descripcion=TXTdesp.getText();
        String Cantidad= TXTcant.getText();
        String IVA= TXTiva.getText();
        String Precio_de_compra= TXTcom.getText();
        String Precio_de_venta= TXTven.getText();
        total=(Double.parseDouble(Precio_de_venta)*Double.parseDouble(IVA)+Double.parseDouble(Precio_de_venta));
        String []datos={Cod_del_producto,Descripcion,Cantidad,IVA,Precio_de_compra,Precio_de_venta,total.toString()};
         tb.addRow(datos);
        conexion articulo=new conexion();
        articulo.InsertarArticulo(Cod_del_producto, Descripcion, Cantidad, IVA, Precio_de_compra, Precio_de_venta);
        
    }//GEN-LAST:event_BTNguardarActionPerformed

