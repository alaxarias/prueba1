    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        // TODO add your handling code here:
        int modificar = tbl_Datos_Clientes.getSelectedRow();

        if(modificar>-1){
            String[] dato={TXTnombre.getText(),TXTapellido.getText(),TXTedad.getText(),TXTcedula.getText(),TXTcupo.getText(), CBB_ocupacion.getSelectedItem().toString()};

            md.insertRow(modificar, dato);
            md.removeRow(modificar+1);
            JOptionPane.showMessageDialog(null, "dato modificado satisfactoriamente");
        } else{
            JOptionPane.showMessageDialog(null, "no hay datos para modificar");
        }

    }//GEN-LAST:event_btnmodificarActionPerformed

