    private void BTNborrarcteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNborrarcteActionPerformed
        // TODO add your handling code here:
        int borrar = tbl_Datos_Clientes.getSelectedRowCount();
        if(borrar>0){
            md.removeRow(borrar-1);
            JOptionPane.showMessageDialog(null, "dato borrado satisfactoriamente");
        } else{
            JOptionPane.showMessageDialog(null, "no hay datos");
        }

    }//GEN-LAST:event_BTNborrarcteActionPerformed

    private void BTNborrarallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNborrarallActionPerformed
        // TODO add your handling code here:

        int borrartodo= tbl_Datos_Clientes.getRowCount();
        for(int i=borrartodo-1;i>-1;i--){
            md.removeRow(i);
            JOptionPane.showMessageDialog(null, "se borro todo los datos");

        }

    }//GEN-LAST:event_BTNborrarallActionPerformed

